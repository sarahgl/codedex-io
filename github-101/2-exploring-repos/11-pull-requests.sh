# Sugerir cambios

# Confirmamos que estamos en la rama 'main'
git branch

# Cambiamos a 'nueva rama' 
git checkout sarahgl/edit

# Preparar, confirmar y subir los cambios a la rama equivalente del repositorio remoto
git add .
commit -m "Sugerir cambios"
git push -u origin sarahgl/edit

